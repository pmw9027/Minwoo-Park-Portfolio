package Admin.info;

public class carInfo {
	private String c_kind;
	private String c_name;
	private int c_fuel;
	private int c_pride;

	public String getC_kind() {
		return c_kind;
	}

	public void setC_kind(String c_kind) {
		this.c_kind = c_kind;
	}

	public String getC_name() {
		return c_name;
	}

	public void setC_name(String c_name) {
		this.c_name = c_name;
	}

	public int getC_fuel() {
		return c_fuel;
	}

	public void setC_fuel(int c_fuel) {
		this.c_fuel = c_fuel;
	}

	public int getC_pride() {
		return c_pride;
	}

	public void setC_pride(int c_pride) {
		this.c_pride = c_pride;
	}

}
